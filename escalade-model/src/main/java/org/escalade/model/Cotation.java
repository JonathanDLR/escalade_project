package org.escalade.model;

public class Cotation {
	private int id;
	private String cot;
	
	// GETTERS SETTERS //
	
	public int getId() {
		return id;
	}
	public void setId(int pId) {
		this.id = pId;
	}
	public String getCot() {
		return cot;
	}
	public void setCot(String pCot) {
		this.cot = pCot;
	}	
}
